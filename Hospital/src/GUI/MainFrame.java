    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        String fname = fnamField.getText();
        String lname = lnameField.getText();
        String email = emailField.getText();
        String phone = pnumField.getText();
        String address = addressField.getText();    
        String status = (String)statusCombo.getSelectedItem();
        String str = "";
        
        if (fname.equals("") || lname.equals("") || staffIDField.getText().equals("") || email.equals("") || phone.equals("") || 
                address.equals("") || countField.getText().equals("") || multiplierField.getText().equals("")){
            str = ("Please fill all necessary fields!");
        }
        else{
            int count = Integer.parseInt(countField.getText());
            double multiplier = Double.parseDouble(multiplierField.getText());
            int id = Integer.parseInt(staffIDField.getText()); 

            String type = (String)TypeCombo.getSelectedItem();
            str = HospSys.addStaff(type, id, fname, lname, email, phone, address, count, status, multiplier);
            clear();
        }
        output.setText(str);
    }//GEN-LAST:event_addActionPerformed

