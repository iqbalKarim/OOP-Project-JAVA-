    private void TypeComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TypeComboActionPerformed
        String type = (String)TypeCombo.getSelectedItem();
        switch (type) {
            case "Surgeon":
                countLabel.setText("Num Surgeries:");
                multiplierLabel.setText("Per Surgery:");
                status.setText("Speciality:");
                String cmbs[] = new String[5];
                cmbs[0] = "Cardiologist";
                cmbs[1] = "Orthodontist";
                cmbs[2] = "Neurosurgeon";
                cmbs[3] = "Obstetrician";
                cmbs[4] = "Orthopaedic";

                statusCombo.setModel(new DefaultComboBoxModel(cmbs));
                break;
            case "Consultant":
                countLabel.setText("Num Consultancies:");
                multiplierLabel.setText("Per Consultancy:");
                status.setText("Speciality:");
                String cmbc[] = new String[4];
                cmbc[0] = "Cardiologist";
                cmbc[1] = "Pediatrician";
                cmbc[2] = "Dermatologists";
                cmbc[3] = "Nutritionist";
                statusCombo.setModel(new DefaultComboBoxModel(cmbc));
                break;
            case "Nurse":
                countLabel.setText("Hours:");
                multiplierLabel.setText("Wage:");
                status.setText("Lisence:");
                String cmbn[] = new String[2];
                cmbn[0] = "true";
                cmbn[1] = "false";
                statusCombo.setModel(new DefaultComboBoxModel(cmbn));

                break;
            default:
                break;
        }
    }//GEN-LAST:event_TypeComboActionPerformed

